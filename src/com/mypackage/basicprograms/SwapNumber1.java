package com.mypackage.basicprograms;

//Wap to swap between two numbers with third variables?
public class SwapNumber1
{
    public static void main(String[] args)
    {
            int a=10,b=20;
            int t;
            t=a;
            a=b;
            b=t;
        System.out.println("a : "+a);
        System.out.println("b : "+b);
    }


}
